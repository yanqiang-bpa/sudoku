    public int[] matrix_flag=new int[81];
    public int puzzleMatrix[]=new int[81];

    public void fromStringToMatrix(String mode){
        String[] tmp=new String[81];
        switch (mode) {
            case "": tmp=this.easyPuzzleString;break;
            case "easy": tmp=this.easyPuzzleString;break;
            case "medium": tmp=this.mediumPuzzleString;break;
            case "hard": tmp=this.hardPuzzleString;break;
            default: break;
        }

        int randomPuzzleIndex= new Random().nextInt(6);

        for(int i=0;i<81; i++){
            puzzleMatrix[i]=tmp[randomPuzzleIndex].charAt(i)-'0';
            if(puzzleMatrix[i]!=0)
                matrix_flag[i]=0;
            //former position is 0, so the cell flag==1 means player can modify its number
            else matrix_flag[i]=1;

        }
    }

